function enrichQuestion(q) {
  return {
    ...q,
    packIds: q.packIds || LEGACY_QUESTION_PACK_MAP[q.id] || [],
    sessionHint: q.sessionHint ?? LEGACY_QUESTION_SESSION_HINT[q.id] ?? null,
  };
}

